    public function alterarPermissao(Request $request)
    {
        try {
            DB::beginTransaction();
            $usuario = new Usuario();
            $usr = $usuario->where('id', $request->usuario_id)->first();
            $usr->nivel_de_acesso = $request->acesso;
            $usr->save();
            DB::commit();
        } catch(\Exception $e) {
            echo $e->getMessage();
            DB::rollback();
        }

        return redirect()->back();
    }

